public class App {
    public static void main(String[] args) throws Exception {
        int a = 5;
        int b = 10;
        int sum = a + b;
        System.out.println("The sum of " + a + " and " + b + " is: " + sum);

        int subtraction = a - b;
        System.out.println("The difference between " + a + " and " + b + " is: " + subtraction);
        
    }
}
